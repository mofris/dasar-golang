package main

import "fmt"

func main() {
	var name string
	name = "Faris"

	fmt.Println(name)

	// tipe data integer
	var age = 20
	fmt.Println(age)

	// tipe data string
	var namaku = "Mochammad Faris"
	fmt.Println(namaku)
	
	dataku := "Faris"
	fmt.Println(dataku)

	// deklarasi multiple
	var (
		firstname = "Mochammad"
		lastname = "Faris"
	)

	fmt.Println(firstname)
	fmt.Println(lastname)
}